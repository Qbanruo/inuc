        <p>（4）发病 90 天后，经检查证实左心室功能降低的，如左心室射血分数低于 50%。</p>
        <h4>3、脑中风后遗症</h4>
        <p> 指因脑血管的突发病变引起脑血管出血、栓塞或梗塞，并导致神经系统永久性功能 障碍。
          神经系统永久性的功能障碍，指疾病确诊 180 天后，仍遗留下列一种或一种 以上障碍：</p>
        <p>（1）一肢或一肢以上肢体机能完全丧失；</p>
        <p>（2）语言能力或咀嚼吞咽能力完全丧失；</p>
        <p>（3）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <h4>4、重大器官移植术或造血干细胞移植术重大器官移植术，指因相应器官功能衰竭，已经实
          施了肾脏、肝脏、心脏或肺脏的 异体移植手术。造血干细胞移植术，指因造血功能损害或
          造血系统恶性肿瘤，已经实施了造血干细胞（包括骨髓造血干细胞、外周血造血干细胞和脐
          血造血干细胞）的异体移植手术。</h4>
        <h4> 5、冠状动脉搭桥术</h4>
        <p>指为治疗严重的冠心病，实际实施了开胸进行的冠状动脉血管旁路移植的手术。冠状动脉
          支架植入手术、心导管球囊扩张术、激光射频技术及其他非开胸的介入手术、腔镜手术不在
          保障范围内。</p>
        <h4> 6、终末期肾病</h4>
        <p>指双肾功能慢性不可逆性衰竭，达到尿毒症期，经诊断后已经进行了至少 90 天的规律性
          透析治疗或实施了肾脏移植手术。</p>
        <h4>7、多个肢体缺失</h4>
        <p>指因疾病或意外伤害导致两个或两个以上肢体自腕关节或踝关节近端（靠近躯干 端）以上
          完全性断离。</p>
        <h4> 8、急性或亚急性重症肝炎指因肝炎病毒感染引起肝脏组织弥漫性坏死，导致急性肝功能衰
          竭，且经血清学或病毒学检查证实，并须满足下列全部条件：</h4>
        <p>（1）重度黄疸或黄疸迅速加重；</p>
        <p>（2）肝性脑病；</p>
        <p>（3）B 超或其他影像学检查显示肝脏体积急速萎缩；</p>
        <p>（4）肝功能指标进行性恶化。</p>
        <h4> 9、良性脑肿瘤</h4>
        <p>指脑的良性肿瘤，已经引起颅内压增高，临床表现为视神经乳头水肿、精神症状、癫痫及
          运动感觉障碍等，并危及生命。须由头颅断层扫描（CT）、核磁共振检查（MRI）或正电子发
          射断层扫描（PET）等影像学检查证实，并须满足下列至少一项条件：</p>
        <p>（1）实际实施了开颅进行的脑肿瘤完全切除或部分切除的手术；</p>
        <p>（2）实际实施了对脑肿瘤进行的放射治疗。脑垂体瘤、脑囊肿、脑血管性疾病不在保障范
          围内。</p>
        <h4> 10、慢性肝功能衰竭失代偿期</h4>
        <p> 指因慢性肝脏疾病导致肝功能衰竭，须满足下列全部条件：</p>
        <p>（1）持续性黄疸；</p>
        <p>（2）腹水；</p>
        <p>（3）肝性脑病；</p>
        <p>（4）充血性脾肿大伴脾功能亢进或食管胃底静脉曲张。因酗酒或药物滥用导致的肝功能衰
          竭不在保障范围内。</p>
        <h4>11、脑炎后遗症或 脑膜炎后遗症 指因患脑炎或脑膜炎导致的神经系统永久性的功能障碍。
          神经系统永久性的功能障碍，指疾病确诊 180 天后，仍残留下列一种或一种以上障碍：</h4>
        <p>（1）一肢或一肢以上肢体机能完全丧失；</p>
        <p>（2）语言能力或咀嚼吞咽能力完全丧失；</p>
        <p>（3）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <h4>12、深度昏迷</h4>
        <p>指因疾病或意外伤害导致意识丧失,对外界刺激和体内需求均无反应，昏迷程度按 照格拉
          斯哥昏迷分级（Glasgow coma scale）结果为 5 分或 5 分以下，且已经持续使用呼吸机及其
          它生命维持系统 96 小时以上。由于酗酒或药物滥用导致的深度昏迷不在保障范围内。</p>
        <h4> 13、双耳失聪—— 三岁始理赔 指因疾病或意外伤害导致双耳听力永久不可逆性丧失，在
          500 赫兹、1000 赫兹和 2000 赫兹语音频率下，平均听阈大于 90 分贝，且经纯音听力测试、
          声导抗检 测或听觉诱发电位检测等证实。被互助保障人申请理赔时年龄必须在三周岁以上，
          并且提供理赔当时的听力丧失诊断 及检查证据。</h4>
        <h4> 14、双目失明—— 三岁始理赔</h4>
        <p>指因疾病或意外伤害导致双眼视力永久不可逆性丧失，双眼中较好眼须满足下列至少一项
          条件：</p>
        <p>（1）眼球缺失或摘除；</p>
        <p> （2）矫正视力低于 0.02（采用国际标准视力表，如果使用其他视力表应进行换算）；</p>
        <p>（3）视野半径小于 5 度。被互助保障人申请理赔时年龄必须在三周岁以上，并且提供理赔</p>
        <p> 当时的视力丧失诊断及检查证据。</p>
        <h4>15、瘫痪</h4>
        <p> 指因疾病或意外伤害导致两肢或两肢以上肢体机能永久完全丧失。肢体机能永久完 全丧
          失，指疾病确诊 180 天后或意外伤害发生 180 天后，每肢三大关节中的两大关 节仍然完
          全僵硬，或不能随意识活动。</p>
        <p>16、心脏瓣膜手术指为治疗心脏瓣膜疾病，实际实施了开胸进行的心脏瓣膜置换或修复的
          手术。</p>
        <h4> 17、严重阿尔茨海默病</h4>
        <p> 指因大脑进行性、不可逆性改变导致智能严重衰退或丧失，临床表现为明显的认知 能力障
          碍、行为异常和社交能力减退，其日常生活必须持续受到他人监护。须由头 颅断层扫描（CT）、
          核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学 检查证实，自主生活能力完
          全丧失，无法独立完成六项基本日常生活活动中的三项 或三项以上。神经官能症和精神疾
          病不在保障范围内。</p>
        <h4> 18、严重脑损伤</h4>
        <p>指因头部遭受机械性外力，引起脑重要部位损伤，导致神经系统永久性的功能障碍。须由
          头颅断层扫描（CT）、核磁共振检查（MRI）或正电子发射断层扫描（PET）等影像学检查证
          实。神经系统永久性的功能障碍，指脑损伤 180 天后，仍遗留下列一种或一种以上障碍：</p>
        <p>（1）一肢或一肢以上肢体机能完全丧失；</p>
        <p>（2）语言能力或咀嚼吞咽能力完全丧失；</p>
        <p>（3）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <h4>19、严重帕金森病</h4>
        <p> 指一种中枢神经系统的退行性疾病，临床表现为震颤麻痹、共济失调等。须满足下列全部
          条件：</p>
        <p> （1）药物治疗无法控制病情；</p>
        <p>（2）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。
          继发性帕金森综合征不在保障范围内。</p>
        <h4>20、严重 III 度烧伤指烧伤程度为Ⅲ度，且Ⅲ度烧伤面积达到全身体表面积的 20％或 20％
          以上。体表面积根据《中国新九分法》计算。</h4>
        <h4> 21、严重原发性肺 动脉高压指不明原因的肺动脉压力持续性增高，进行性发展而导致的慢
          性疾病，已经造成永久不可逆性的体力活动能力受限，达到美国纽约心脏病学会心功能状态
          分级 IV 级，静息状态下肺动脉平均压超过 30mmHg。</h4>
        <h4> 22、严重运动神经元病</h4>
        <p>是一组中枢神经系统运动神经元的进行性变性疾病，包括进行性脊肌萎缩症、进行 性延髓
          麻痹症、原发性侧索硬化症、肌萎缩性侧索硬化症。须满足自主生活能力完 全丧失，无法
          独立完成六项基本日常生活活动中的三项或三项以上。
        <h4> 23、语言能力丧失，三岁始可申请互助金</h4>
        <p>指因疾病或意外伤害导致完全丧失语言能力，经过积极治疗至少 12 个月（声带完全 切除
          不受此时间限制），仍无法通过现有医疗手段恢复。精神心理因素所致的语言能力丧失不在
          保障范围内。被互助保障人申请理赔时年龄必须在三周岁以上，并且提供理赔当时的语言能
          力丧失诊断及检查证据。</p>
        <h4> 24、重型再生障碍性贫血</h4>
        <p>指因骨髓造血功能慢性持续性衰竭导致的贫血、中性粒细胞减少及血小板减少，须满足下
          列全部条件：</p>
        <p>（1）骨髓穿刺检查或骨髓活检结果支持诊断；</p>
        <p>（2）外周血象必须具备以下三项条件：</p>
        <p> ①中性粒细胞绝对值≤0.5×109/L；</p>
        <p>②网织红细胞＜1%；</p>
        <p> ③血小板绝对值≤20×109/L。</p>
        <h4>25、主动脉手术</h4>
        <p>指为治疗主动脉疾病，实际实施了开胸或开腹进行的切除、置换、修补病损主动脉血管的
          手术。主动脉指胸主动脉和腹主动脉，不包括胸主动脉和腹主动脉的分支血 管。动脉内血
          管成形术不在保障范围内。</p>
        <h4>26、慢性呼吸功能 衰竭慢性肺部疾病导致永久不可逆性的慢性呼吸功能衰竭。慢性呼吸功
          能衰竭必须明确诊断并满足下列全部条件：</h4>
        <p>（1）休息时出现呼吸困难；</p>
        <p>（2）动脉血氧分压 （PaO2）< 50mmHg；</p>
        <p>（3）动脉血氧饱和度 （SaO2）< 80％；</p>
        <p>（4）因缺氧必须接受持续的输氧治疗。</p>
        <h4>27、严重多发性硬化</h4>
        <p>多发性硬化为中枢神经系统白质多灶性脱髓鞘病变，病变有时累及灰质。多发性硬化必须
          明确诊断，并且已经造成永久不可逆的神经系统功能损害。永久不可逆的神经系统损害指被
          互助保障人持续 180 天无法独立完成下列基本日常生活活动：</p>
        <p>（1）移动：自己从一个房间到另一个房间；或者</p>
        <p>（2）进食：自己从已准备好的碗或碟中取食物放入口中。</p>
        <h4>28、重症肌无力</h4>
        <p>重症肌无力是一种神经肌肉接头传递障碍所致的疾病，表现为局部或全身骨骼肌 （特别是
          眼外肌）极易疲劳。疾病可以累及呼吸肌、上肢或下肢的近端肌群或全身 肌肉，必须满足
          下列全部条件：</p>
        <p>（1）经药物治疗和胸腺切除治疗一年以上仍无法控制病情；</p>
        <p>（2）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三 项以上。</p>
        <h4>29、严重心肌病</h4>
        <p>指不明原因引起的一类心肌病变，包括原发性扩张型心肌病、原发性肥厚型心肌病 及原发
          性限制型心肌病三种，本病须经专科医生明确诊断，且病变必须已造成事实 上心室功能障
          碍而出现明显的心功能衰竭（指按照美国纽约心脏协会心功能分类标 准心功能达四级*），
          且有相关住院医疗记录及检查显示四级心功能衰竭状态持续至少 180 天。继发于全身性疾
          病或其它器官系统疾病以及由于酒精和药物滥用造成 的心肌病变不在保障范围内。</p>
        <h4>30、严重类风湿性关节炎</h4>
        <p> 指类风湿性关节炎患者必须符合以下三项标准:</p>
        <p>（1）至少包括下列关节中的三组或以上有广泛受损和畸形改变:手指关节、腕关节、 肘关
          节、膝关节、髋关节、踝关节、脊椎或脚趾关节；</p>
        <p> （2）手和腕的后前位 X 线拍片检查可见类风湿性关节炎的典型变化，包括骨质侵 蚀或钙
          流失，位于受累关节及其邻近部位尤其明显；</p>
        <p>（3）关节的畸形改变至少持续 6 个月。必须经专科医生明确诊断，且首次已接受膝关节或
          髋关节置换手术。</p>
        <h4>31、系统性红斑狼疮 III 型或以上狼疮性肾炎
          狼疮性肾炎是系统性红斑狼疮的一种并发症。系统性红斑狼疮是由多种因素引起，累及多
          系统的自身免疫性疾病，其特点是生成自身抗体对抗多种自身抗原。狼疮性肾炎是指系统性
          红斑狼疮患者并发狼疮性肾炎而影响和损害肾脏功能。世界卫生组织（WHO）狼疮性肾炎
          分型：</h4>
        <p>严重狼疮性肾炎是指经肾脏活检确认的，符合 WHO 诊断标准定义 III 型、IV 型、V 型和Ⅵ
          型的狼疮性肾炎,且肌酐清除率持续低于 30ml/分。其他类型的红斑性狼疮，如盘状狼疮、仅
          累及血液及关节的狼疮不在本协议保障范围內。本病必须由免疫和风湿科专家医师确诊。
          I 型（微小病变型）镜下阴性，尿液正常 II 型（系膜病变型）中度蛋白尿，偶有尿沉渣改变
          III 型（局灶及节段增生型）蛋白尿，尿沉渣改变 IV 型（弥漫增生型）急性肾炎伴有尿沉渣
          改变及／或肾病综合征 V 型（膜型）肾病综合征或重度蛋白尿Ⅵ型（肾小球硬化型）肾小球
          硬化成纤维团状，肾功能差，无法恢复。</p>
        <h4>32、严重克隆病</h4>
        <p>克隆病是一种慢性肉芽肿性肠炎，具有特征性的克隆病病理组织学变化。诊断必须由病理
          检查结果证实。被互助保障人所患的克隆病必须已经造成瘘管形成并伴有肠梗阻 或肠穿孔。</p>
        <h4>33、急性坏死性胰腺炎</h4>
        <p>是指由本社区认可的专科医师确诊为急性坏死性胰腺炎，需进行坏死组织清除、病 灶切除
          或胰腺部分切除的手术治疗，但不包括因酒精中毒引起的急性坏死性胰腺 炎。</p>
        <h4>34、肌营养不良症</h4>
        <p> 指一组原发于肌肉的遗传性疾病，临床表现为与神经系统无关的肌肉无力和肌肉萎缩。且
          须满足下列全部条件：</p>
        <p> （1）肌肉组织活检结果满足肌营养不良症的肌肉细胞变性、坏死等阳性改变；</p>
        <p>（2）自主生活能力完全丧失，无法独立完成六项基本日常生活活动中的三项或三项以上。</p>
        <h4>35、脊髓灰质炎</h4>
        <p>脊髓灰质炎是由于脊髓灰质炎病毒感染所致的瘫痪性疾病，临床表现为运动功能损 害或呼
          吸无力。脊髓灰质炎必须明确诊断。本互助计划仅对脊髓灰质炎造成的神经系统 功能损害
          导致被互助保障人一肢或一肢以上肢体机能永久完全丧失的情况予以理赔。肢体机能永久完
          全丧失，指疾病确诊 180 天后，每肢三大关节中的两大关节仍然完全 僵硬，或不能随意识
          活动。</p>
        <h4>36、终末期肺病慢性肺部疾病导致永久不可逆性的慢性呼吸功能衰竭，必须由呼吸专科医
          师确认并 符合下列所有条件：</h4>
        <p>（1）肺功能测试其 FEV1 持续低于 0.75 升；</p>
