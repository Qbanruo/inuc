      vueQr
    },
    data () {
      return {
        dialogFormVisible: false,
        innerVisible: false,
        imageUrl: '../../../static/img/logo_qr.png',
        codeUrl: '',
        dotScale: 0.6,
        form: {
          year: '',
          amount: '',
          money: '',
        },
        termForm: {
          term: ''
        },
        years: [],
        inuInfo: null,
        params: {
          token: window.sessionStorage.getItem('token'),
          time:''
        },
        timeInterval: null
      }
    },
    created () {
      this.inuInfo = null
      this.years = []
      this.getINU()
    },
    beforeRouteLeave (to, from, next) {
      clearInterval(this.timeInterval)
      next()
    },
    methods: {
      show () {
        this.dialogFormVisible = true
      },
      getINU () {
        api.getInuInfoByType({type: 100}).then(s => {
          this.inuInfo = s.data
          s.data.forEach(p => {
            this.years.push(p.year)
          })
        })
      },
      async validate () {
        let termForm = false
        let form = false
        this.$refs.termForm.validate((v) => {
          termForm = v
        })
        this.$refs.form.validate((valid) => {
          form = valid
        })
        if (form && termForm) {
          this.submit()
        }
      },
      submit () {
        let params = {
          token: window.sessionStorage.getItem('token'),
          amount: this.form.amount,
          type: 100,
          year: this.form.year
        }
        api.postSweepPayment(params).then(s => {
          let codeUrl = s.data
          this.params.time = s.time
          if(codeUrl){
            this.innerVisible = true
            this.$nextTick(() => {
              this.codeUrl = codeUrl
              setTimeout(() =>{
                this.sweepCallBack()
              }, 5000)
            })
          }
        })
      },
      sweepCallBack(){
        let vue = this
        this.timeInterval = setInterval(() => {
          api.sweepCallBack(vue.params).then(s => {
            console.log('sweepCallBack-----', s);
          })
        }, 3000)
      }
    },
    watch: {
      'form.year': function (val) {
        if (val) {
          let find = this.inuInfo.find(s => {
            return s.year === val
          })
          this.form.amount = find.amount
          if (window.sessionStorage.getItem("inuPrice")) {
            this.form.money = window.sessionStorage.getItem("inuPrice") * find.amount
          }
        }
      },
